class Gallery extends Component {
  state = {imageId: imagesList[0].id}

  updateStateValue = id => {
    this.setState({imageId: id})
  }

  filterImagesList = () => {
    const {imageId} = this.state

    const result = imagesList[imageId]
    return result
  }

  render() {
    const {imageId} = this.state

    const filteredImage = this.filterImagesList()

    const {imageAltText, imageUrl} = filteredImage

    return (
      <div className="bg-container">
        <div className="card">
          <img className="img" src={imageUrl} alt={imageAltText} />

          <h1 className="heading">Nature Photography</h1>
          <p className="description">Nature Photography by Venkatesh</p>
          <ul className="list-container">
            {imagesList.map(each => (
              <ThumbnailItem
                thumbnailImageDetails={each}
                key={each.id}
                updateStateValue={this.updateStateValue}
                isActive={each.id === imageId}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}

export default Gallery
